    private void addNewTenantButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewTenantButtonActionPerformed
        // TODO add your handling code here:
        boolean valid = true;
        String id = tenantIDTextField.getText();
        String name = tenantNameTextField.getText();
        String apNum = apartmentNumberTextField.getText();
        String phone = phoneNumberTextField.getText();
        String email = emailAddressTextField.getText();
        String checkIn = checkInTextField.getText();
        String checkOut = checkOutTextField.getText();
        String password = passwordTextField.getText();
        if (name.equals("") || apNum.equals("") || phone.equals("") || email.equals("") || 
                checkIn.equals("") || checkOut.equals("")){
            valid = false;
            
        }
        if (valid == true){
            TenantAccount Ten = new TenantAccount(id, name, apNum, phone, email, checkIn, checkOut, password);
            Tenant.addTenant(Ten);
            updateAll();
            tenantNameTextField.setText("");
            apartmentNumberTextField.setText("");
            phoneNumberTextField.setText("");
            emailAddressTextField.setText("");
            checkInTextField.setText("");
            checkOutTextField.setText("");
            newTenantLabel.setText(name + " has been added as a new tenant");
            newTenantLabel.setVisible(true);
        }
        else{
            newTenantLabel.setText("All fields must be completed");
            newTenantLabel.setVisible(true);
        }
        
        
    }//GEN-LAST:event_addNewTenantButtonActionPerformed

    private void updateTenantIDComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTenantIDComboBoxActionPerformed
        // TODO add your handling code here:
        String id = String.valueOf(updateTenantIDComboBox.getSelectedItem());
        ArrayList<TenantAccount> tenantList = Tenant.allTenants();
        String tenantName = " ";
        for (TenantAccount ten: tenantList){
            String tenID = ten.getId();          
            if (tenID.equals(id)){
                tenantName = ten.getName();
            }
        }
        updateTenantStatusLabel.setText("Tenant Name: " + tenantName);
        updateTenantStatusLabel.setVisible(true);
        
    }//GEN-LAST:event_updateTenantIDComboBoxActionPerformed

    private void updateAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAccountButtonActionPerformed
        // TODO add your handling code here:
        String newApNum = newApNumTextField.getText();
        String id = String.valueOf(updateTenantIDComboBox.getSelectedItem());
        boolean apEmpty = true;
        ArrayList<TenantAccount> tenantList = Tenant.allTenants();
        TenantAccount accountToUpdate = tenantList.get(0);
        for (TenantAccount ten: tenantList){
            String apNum = ten.getApNum();
            if (apNum.equals(newApNum)){
                apEmpty = false;
            }
            if(id.equals(ten.getId())){
                accountToUpdate = ten;
            }
        }
        if (apEmpty == true){
            String originalApNum = accountToUpdate.getApNum();
            Tenant.updateTenant(accountToUpdate, newApNum);
            apartmentStatusLabel.setText("Apartment number changed from " + originalApNum + " to " + newApNum + ".");
            apartmentStatusLabel.setVisible(true);
        }
        else{
            apartmentStatusLabel.setText("This apartment is not empty.");
            apartmentStatusLabel.setVisible(true);
        }
        newApNumTextField.setText("");
        
    }//GEN-LAST:event_updateAccountButtonActionPerformed

    private void deleteAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAccountButtonActionPerformed
        // TODO add your handling code here:
        String id = String.valueOf(deleteTenantIDComboBox.getSelectedItem());
        ArrayList<TenantAccount> tenantList = Tenant.allTenants();
        TenantAccount accountToUpdate = tenantList.get(0);
        String name = tenantList.get(0).getName();
        for (TenantAccount ten: tenantList){
            if(id.equals(ten.getId())){
                name = ten.getName();
                accountToUpdate = ten;
            }
        }
        Tenant.deleteTenant(accountToUpdate);
        updateAll();
        deleteTenantStatusLabel.setText("Tenant " + name + "'s account has been deleted");
        deleteTenantStatusLabel.setVisible(true);
    }//GEN-LAST:event_deleteAccountButtonActionPerformed

