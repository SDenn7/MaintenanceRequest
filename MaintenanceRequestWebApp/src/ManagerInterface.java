    public void updateAll(){
        // Fills combo boxes with all tenants
        ArrayList<TenantAccount> tenantList = Tenant.allTenants();
        ArrayList<String> tenantIDList = new ArrayList<String>();
        tenantIDList.add("- Select One -");
        for (TenantAccount ten: tenantList){
            String id = ten.getId();
            tenantIDList.add(id);
        }
        updateTenantIDComboBox.setModel(new javax.swing.DefaultComboBoxModel(tenantIDList.toArray()));
        deleteTenantIDComboBox.setModel(new javax.swing.DefaultComboBoxModel(tenantIDList.toArray()));
        
        if (tenantList.size() >= 1){
            String tenantId = tenantList.get(tenantList.size()-1).getId();
            String tenantNum = tenantId.substring(1);
            int tenNum =  Integer.valueOf(tenantNum);
            String newTenantId = "T"+Integer.toString(tenNum+1);
            tenantIDTextField.setText(newTenantId);
            
            String tenantPassword = tenantList.get(tenantList.size()-1).getPassword();
            String tenantPasswordNum = tenantPassword.substring(1);
            int tenNumPassword = Integer.valueOf(tenantPasswordNum);
            String newTenantPassword = "T" + Integer.toString(tenNumPassword+1);
            passwordTextField.setText(newTenantPassword);
        }
        else{
            tenantIDTextField.setText("T1");
            passwordTextField.setText("T11111111");
        }
        
        // Fills combo boxes will all team members
        ArrayList<TeamMemberAccount> teamList = TeamMember.allTeamMembers();
        ArrayList<String> teamIDList = new ArrayList<String>();
        teamIDList.add("- Select One -");
        for (TeamMemberAccount mtm: teamList){
            String idMtm = mtm.getId();
            teamIDList.add(idMtm);
        }
        deleteTeamMemberComboBox.setModel(new javax.swing.DefaultComboBoxModel(teamIDList.toArray()));
        
        if (teamList.size() >= 1){
            String teamId = teamList.get(teamList.size()-1).getId();
            String teamNum = teamId.substring(1);
            int tNum =  Integer.valueOf(teamNum);
            String newTeamId = "M"+Integer.toString(tNum+1);
            teamMemberIDTextField.setText(newTeamId);
            
            String teamPassword = teamList.get(teamList.size()-1).getPassword();
            String teamPasswordNum = teamPassword.substring(1);
            int teamNumPassword =  Integer.valueOf(teamPasswordNum);
            String newTeamPassword = "M" + Integer.toString(teamNumPassword+1);
            teamPasswordTextField.setText(newTeamPassword);
        }
        else{
            teamMemberIDTextField.setText("M1");
            teamPasswordTextField.setText("M11111111");
        }
    }
    
    

